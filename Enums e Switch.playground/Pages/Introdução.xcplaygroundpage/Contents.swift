## Múltipla escolha

 A vida é cheia de escolhas. Muitas vezes, você precisa escolher uma opção entre várias, e não pode ficar indeciso.
 
 Por exemplo, um restaurante pode oferecer um cardápio de almoço limitado, com três opções de entrada: massa, hambúrguer ou sopa.
 
 Esse sistema funciona bem:

- O gerente do restaurante sabe exatamente quais ingredientes precisam estar disponíveis todos os dias para o almoço, então ele pode encomendar tudo com antecedência.
- A cozinha pode preparar e servir as refeições com rapidez e eficiência.
- Os funcionários não perdem tempo anotando e tentando entender pedidos especiais.
 
 Como programador, você pode aplicar esse mesmo tipo de escolha controlada aos seus programas.
 
