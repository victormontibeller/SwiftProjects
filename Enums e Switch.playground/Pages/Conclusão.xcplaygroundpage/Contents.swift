## Conclusão

 As enumerações são usadas para representar um grupo de valores relacionados. Cada valor possível se chama caso.
 
 Quando você cria uma enum, também cria um novo tipo. As instâncias desse tipo só podem ter valores correspondentes aos dos casos especificados.
 
 Com enums, seu código pode ficar mais fácil de ler e formular, pois os possíveis valores e seus significados ficam sempre claros.
 
 Você pode comparar os valores de enums com `==` ou usando uma instrução switch para testar todos os valores possíveis.
 
 Assim como as instruções if, as instruções switch são outra forma de tomar decisões no código. As instruções switch funcionam muito bem com as enums, mas podem ser usadas para trocar qualquer tipo de valor.
 
 Como as instruções switch devem ser completas, você precisa lidar com todos os valores possíveis. Para lidar com valores que não foram especificados, você pode usar um caso padrão. 
 
 Assim como as estruturas, é possível adicionar propriedades calculadas e métodos às enums.
 
